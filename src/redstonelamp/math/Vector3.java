package redstonelamp.math;

public class Vector3 {
	public int x = 0;
	public int y = 0;
	public int z = 0;
	
	public Vector3() {}
	
	public Vector3(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
	
	public int getZ() {
		return z;
	}
	
	//TODO: Other methods
}
